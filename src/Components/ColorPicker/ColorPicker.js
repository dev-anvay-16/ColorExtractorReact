const reshapeArray = (rows, columns, dataset) => {
  const arr = [];
  for (let x = 0; x < rows; x++) {
    let currRow = [];
    for (let y = 0; y < columns; y++) {
      currRow.push(dataset[x + y]);
    }
    arr.push(currRow);
  }
  return arr;
};

